require 'rack'

router = Router.new
router.draw do
  get Regexp.new("^/?$"), RootController, :root
end

app = Proc.new do |environment|
  request = Rack::Request.new(environment)
  res = Rack::Response.new
  router.run(req, res)
  res.finish
end

Rack::Server.start(
 app: app,
 Port: 3000
)

